/**
 * Save DESIGN.md to project root or specified directory
 */
function saveDesignMd(targetDir, preset = 'modern', options = {}) {
  const content = generateDesignMd(preset, options);
  const filePath = path.join(targetDir, 'DESIGN.md');
  fs.writeFileSync(filePath, content, 'utf8');
  return filePath;
}

